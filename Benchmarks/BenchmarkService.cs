using System.Diagnostics;
using System.Globalization;
using System.Security.Cryptography;
using System.Text;
using MerkleCloudIntegrity.Models;
using MerkleCloudIntegrity.Services;

namespace MerkleCloudIntegrity.Benchmarks;

/// <summary>
/// Defines benchmark entry points for Merkle Tree experiments.
/// </summary>
public sealed class BenchmarkService
{
    private const int DefaultRandomBufferSize = 81920;

    private readonly MerkleTreeService _merkleTreeService;
    private readonly VerificationService _verificationService;
    private readonly TraversalService _traversalService;

    /// <summary>
    /// Initializes a new instance of the <see cref="BenchmarkService"/> class.
    /// </summary>
    /// <param name="merkleTreeService">The Merkle Tree service to benchmark.</param>
    /// <param name="verificationService">The verification service to benchmark.</param>
    /// <param name="traversalService">The traversal service to benchmark.</param>
    public BenchmarkService(
        MerkleTreeService merkleTreeService,
        VerificationService verificationService,
        TraversalService traversalService)
    {
        _merkleTreeService = merkleTreeService;
        _verificationService = verificationService;
        _traversalService = traversalService;
    }

    /// <summary>
    /// Runs traversal comparison benchmarks for classical and cached proof generation.
    /// </summary>
    /// <param name="workingDirectory">The directory where random sample files are generated.</param>
    /// <param name="csvPath">The output CSV path.</param>
    /// <param name="blockSize">The block size used for each benchmark experiment.</param>
    /// <returns>The traversal comparison results generated by the suite.</returns>
    public IReadOnlyList<TraversalBenchmarkResult> RunTraversalBenchmarkSuite(
        string workingDirectory,
        string csvPath,
        int blockSize = CloudIntegrityService.DefaultBlockSize)
    {
        ArgumentException.ThrowIfNullOrWhiteSpace(workingDirectory);
        ArgumentException.ThrowIfNullOrWhiteSpace(csvPath);

        Directory.CreateDirectory(workingDirectory);

        var results = new List<TraversalBenchmarkResult>();
        foreach (var filePath in GenerateBenchmarkFiles(workingDirectory))
        {
            var blocks = _merkleTreeService.SplitFile(filePath, blockSize);
            var root = _merkleTreeService.BuildTree(blocks);
            var targetBlockIndex = ResolveTargetBlockIndex(blocks.Count, 2);

            results.Add(RunTraversalBenchmark(
                "Classical",
                filePath,
                blocks,
                root,
                targetBlockIndex,
                () => _traversalService.GenerateClassicalProof(root, targetBlockIndex)));

            _traversalService.ClearCache();
            results.Add(RunCachedTraversalBenchmark(filePath, blocks, root, targetBlockIndex));
        }

        ExportTraversalComparisonToCsv(results, csvPath);
        return results;
    }

    /// <summary>
    /// Runs the scalability benchmark suite for the predefined file sizes.
    /// </summary>
    /// <param name="workingDirectory">The directory where random sample files are generated.</param>
    /// <param name="csvPath">The output CSV path.</param>
    /// <param name="blockSize">The block size used for each benchmark experiment.</param>
    /// <returns>The benchmark results generated by the suite.</returns>
    public IReadOnlyList<BenchmarkResult> RunScalabilityBenchmarkSuite(
        string workingDirectory,
        string csvPath,
        int blockSize = CloudIntegrityService.DefaultBlockSize)
    {
        ArgumentException.ThrowIfNullOrWhiteSpace(workingDirectory);
        ArgumentException.ThrowIfNullOrWhiteSpace(csvPath);

        Directory.CreateDirectory(workingDirectory);

        var results = GenerateBenchmarkFiles(workingDirectory)
            .Select(filePath => RunFileBenchmark(filePath, blockSize))
            .ToList();

        ExportToCsv(results, csvPath);
        return results;
    }

    /// <summary>
    /// Runs a file-based Merkle Tree benchmark.
    /// </summary>
    /// <param name="filePath">The path of the benchmark input file.</param>
    /// <param name="blockSize">The block size used for file splitting.</param>
    /// <param name="targetBlockIndex">The preferred target block index for proof generation.</param>
    /// <returns>The measured benchmark result.</returns>
    public BenchmarkResult RunFileBenchmark(string filePath, int blockSize, int targetBlockIndex = 2)
    {
        ArgumentException.ThrowIfNullOrWhiteSpace(filePath);
        EnsurePositiveBlockSize(blockSize);

        GC.Collect();
        GC.WaitForPendingFinalizers();
        GC.Collect();
        var memoryBefore = GC.GetTotalMemory(forceFullCollection: true);

        var blocks = _merkleTreeService.SplitFile(filePath, blockSize);
        var measuredBlockIndex = ResolveTargetBlockIndex(blocks.Count, targetBlockIndex);

        var stopwatch = Stopwatch.StartNew();
        var root = _merkleTreeService.BuildTree(blocks);
        stopwatch.Stop();
        var buildTimeMs = stopwatch.Elapsed.TotalMilliseconds;

        stopwatch.Restart();
        var proof = _merkleTreeService.GenerateProof(root, measuredBlockIndex);
        stopwatch.Stop();
        var proofGenerationTimeMs = stopwatch.Elapsed.TotalMilliseconds;

        stopwatch.Restart();
        _verificationService.VerifyProof(blocks[measuredBlockIndex], proof, root.Hash);
        stopwatch.Stop();
        var verificationTimeMs = stopwatch.Elapsed.TotalMilliseconds;

        var memoryAfter = GC.GetTotalMemory(forceFullCollection: true);

        return new BenchmarkResult
        {
            FileName = Path.GetFileName(filePath),
            BlockSize = blockSize,
            BlockCount = blocks.Count,
            BuildTimeMs = buildTimeMs,
            ProofGenerationTimeMs = proofGenerationTimeMs,
            VerificationTimeMs = verificationTimeMs,
            MemoryUsageBytes = Math.Max(0, memoryAfter - memoryBefore),
            MerkleRoot = root.Hash
        };
    }

    /// <summary>
    /// Exports a benchmark result to a CSV file.
    /// </summary>
    /// <param name="result">The benchmark result to export.</param>
    /// <param name="csvPath">The output CSV path.</param>
    /// <returns>The path of the generated CSV file.</returns>
    public string ExportToCsv(BenchmarkResult result, string csvPath)
    {
        ArgumentNullException.ThrowIfNull(result);
        return ExportToCsv(new[] { result }, csvPath);
    }

    /// <summary>
    /// Exports benchmark results to a CSV file.
    /// </summary>
    /// <param name="results">The benchmark results to export.</param>
    /// <param name="csvPath">The output CSV path.</param>
    /// <returns>The path of the generated CSV file.</returns>
    public string ExportToCsv(IReadOnlyList<BenchmarkResult> results, string csvPath)
    {
        ArgumentNullException.ThrowIfNull(results);

        var csv = new StringBuilder();
        csv.AppendLine("FileName,BlockSize,BlockCount,BuildTimeMs,ProofGenerationTimeMs,VerificationTimeMs,MemoryUsageBytes,MerkleRoot");

        foreach (var result in results)
        {
            csv.AppendLine(string.Join(
                ',',
                EscapeCsv(result.FileName),
                result.BlockSize.ToString(CultureInfo.InvariantCulture),
                result.BlockCount.ToString(CultureInfo.InvariantCulture),
                result.BuildTimeMs.ToString("F4", CultureInfo.InvariantCulture),
                result.ProofGenerationTimeMs.ToString("F4", CultureInfo.InvariantCulture),
                result.VerificationTimeMs.ToString("F4", CultureInfo.InvariantCulture),
                result.MemoryUsageBytes.ToString(CultureInfo.InvariantCulture),
                EscapeCsv(result.MerkleRoot)));
        }

        return WriteCsv(csvPath, csv);
    }

    /// <summary>
    /// Exports traversal comparison benchmark results to a CSV file.
    /// </summary>
    /// <param name="results">The traversal benchmark results to export.</param>
    /// <param name="csvPath">The output CSV path.</param>
    /// <returns>The path of the generated CSV file.</returns>
    public string ExportTraversalComparisonToCsv(IReadOnlyList<TraversalBenchmarkResult> results, string csvPath)
    {
        ArgumentNullException.ThrowIfNull(results);

        var csv = new StringBuilder();
        csv.AppendLine("TraversalMode,FileName,BlockCount,ProofGenerationTimeMs,VerificationTimeMs,MemoryUsageBytes");

        foreach (var result in results)
        {
            csv.AppendLine(string.Join(
                ',',
                EscapeCsv(result.TraversalMode),
                EscapeCsv(result.FileName),
                result.BlockCount.ToString(CultureInfo.InvariantCulture),
                result.ProofGenerationTimeMs.ToString("F4", CultureInfo.InvariantCulture),
                result.VerificationTimeMs.ToString("F4", CultureInfo.InvariantCulture),
                result.MemoryUsageBytes.ToString(CultureInfo.InvariantCulture)));
        }

        return WriteCsv(csvPath, csv);
    }

    private TraversalBenchmarkResult RunCachedTraversalBenchmark(
        string filePath,
        IReadOnlyList<DataBlock> blocks,
        MerkleNode root,
        int targetBlockIndex)
    {
        GC.Collect();
        GC.WaitForPendingFinalizers();
        GC.Collect();
        var memoryBefore = GC.GetTotalMemory(forceFullCollection: true);

        _traversalService.GenerateCachedProof(root, targetBlockIndex);

        var stopwatch = Stopwatch.StartNew();
        var proof = _traversalService.GenerateCachedProof(root, targetBlockIndex);
        stopwatch.Stop();
        var proofGenerationTimeMs = stopwatch.Elapsed.TotalMilliseconds;

        stopwatch.Restart();
        _verificationService.VerifyProof(blocks[targetBlockIndex], proof, root.Hash);
        stopwatch.Stop();
        var verificationTimeMs = stopwatch.Elapsed.TotalMilliseconds;

        var memoryAfter = GC.GetTotalMemory(forceFullCollection: true);

        return new TraversalBenchmarkResult
        {
            TraversalMode = "Cached",
            FileName = Path.GetFileName(filePath),
            BlockCount = blocks.Count,
            ProofGenerationTimeMs = proofGenerationTimeMs,
            VerificationTimeMs = verificationTimeMs,
            MemoryUsageBytes = Math.Max(0, memoryAfter - memoryBefore)
        };
    }

    private TraversalBenchmarkResult RunTraversalBenchmark(
        string traversalMode,
        string filePath,
        IReadOnlyList<DataBlock> blocks,
        MerkleNode root,
        int targetBlockIndex,
        Func<IReadOnlyList<ProofItem>> proofFactory)
    {
        GC.Collect();
        GC.WaitForPendingFinalizers();
        GC.Collect();
        var memoryBefore = GC.GetTotalMemory(forceFullCollection: true);

        var stopwatch = Stopwatch.StartNew();
        var proof = proofFactory();
        stopwatch.Stop();
        var proofGenerationTimeMs = stopwatch.Elapsed.TotalMilliseconds;

        stopwatch.Restart();
        _verificationService.VerifyProof(blocks[targetBlockIndex], proof, root.Hash);
        stopwatch.Stop();
        var verificationTimeMs = stopwatch.Elapsed.TotalMilliseconds;

        var memoryAfter = GC.GetTotalMemory(forceFullCollection: true);

        return new TraversalBenchmarkResult
        {
            TraversalMode = traversalMode,
            FileName = Path.GetFileName(filePath),
            BlockCount = blocks.Count,
            ProofGenerationTimeMs = proofGenerationTimeMs,
            VerificationTimeMs = verificationTimeMs,
            MemoryUsageBytes = Math.Max(0, memoryAfter - memoryBefore)
        };
    }

    private static IReadOnlyList<string> GenerateBenchmarkFiles(string workingDirectory)
    {
        return new[]
        {
            GenerateRandomSampleFile(workingDirectory, "sample-64kb.bin", 64 * 1024),
            GenerateRandomSampleFile(workingDirectory, "sample-256kb.bin", 256 * 1024),
            GenerateRandomSampleFile(workingDirectory, "sample-1mb.bin", 1024 * 1024),
            GenerateRandomSampleFile(workingDirectory, "sample-5mb.bin", 5 * 1024 * 1024),
            GenerateRandomSampleFile(workingDirectory, "sample-10mb.bin", 10 * 1024 * 1024)
        };
    }

    private static string GenerateRandomSampleFile(string directory, string fileName, int sizeBytes)
    {
        var filePath = Path.Combine(directory, fileName);
        var buffer = new byte[Math.Min(DefaultRandomBufferSize, sizeBytes)];
        var remainingBytes = sizeBytes;

        using var fileStream = File.Create(filePath);
        while (remainingBytes > 0)
        {
            var bytesToWrite = Math.Min(buffer.Length, remainingBytes);
            RandomNumberGenerator.Fill(buffer.AsSpan(0, bytesToWrite));
            fileStream.Write(buffer, 0, bytesToWrite);
            remainingBytes -= bytesToWrite;
        }

        return filePath;
    }

    private static int ResolveTargetBlockIndex(int blockCount, int targetBlockIndex)
    {
        if (blockCount == 0)
        {
            throw new InvalidOperationException("The source file did not produce any data blocks.");
        }

        return targetBlockIndex >= 0 && targetBlockIndex < blockCount
            ? targetBlockIndex
            : blockCount - 1;
    }

    private static void EnsurePositiveBlockSize(int blockSize)
    {
        if (blockSize <= 0)
        {
            throw new ArgumentOutOfRangeException(nameof(blockSize), "Block size must be greater than zero.");
        }
    }

    private static string WriteCsv(string csvPath, StringBuilder csv)
    {
        ArgumentException.ThrowIfNullOrWhiteSpace(csvPath);

        var directory = Path.GetDirectoryName(csvPath);
        if (!string.IsNullOrWhiteSpace(directory))
        {
            Directory.CreateDirectory(directory);
        }

        File.WriteAllText(csvPath, csv.ToString(), Encoding.UTF8);
        return csvPath;
    }

    private static string EscapeCsv(string value)
    {
        if (!value.Contains(',') && !value.Contains('"') && !value.Contains('\n') && !value.Contains('\r'))
        {
            return value;
        }

        return $"\"{value.Replace("\"", "\"\"", StringComparison.Ordinal)}\"";
    }
}
